// Sell prices (what we pay when customers SELL TO us)
// Premium values here are NEGATIVE - we pay below spot

export interface SellProduct {
  id: string;
  name: string;
  subtitle?: string;
  metalType: 'gold' | 'silver' | 'platinum';
  category: 'coins' | 'bars' | 'rounds' | 'fractional' | 'junk' | 'novelty' | 'european';
  weight: number;
  weightUnit: 'oz' | 'gram';
  purity: string;
  premiumType: 'percent' | 'fixed';
  premiumAmount: number; // Negative for sell prices (we pay below spot)
  country: string;
  mint?: string;
  year: string;
  description: string;
  image: string;
  isPopular?: boolean;
}

// Calculate sell price (what we pay customer)
export function calculateSellPrice(product: SellProduct, spotPrices: { gold: number; silver: number; platinum: number }): number {
  let spot: number;
  if (product.metalType === 'gold') {
    spot = spotPrices.gold;
  } else if (product.metalType === 'platinum') {
    spot = spotPrices.platinum;
  } else {
    spot = spotPrices.silver;
  }

  // Convert grams to oz if needed
  const weightInOz = product.weightUnit === 'gram' ? product.weight / 31.1035 : product.weight;
  const spotValue = spot * weightInOz;

  if (product.premiumType === 'percent') {
    // Negative percent means we pay less than spot
    return spotValue * (1 + product.premiumAmount / 100);
  } else {
    // Negative fixed amount means we pay less than spot
    return spotValue + product.premiumAmount;
  }
}

// Format premium for sell display
export function formatSellPremium(product: SellProduct): string {
  if (product.premiumType === 'percent') {
    const sign = product.premiumAmount >= 0 ? '+' : '';
    return `${sign}${product.premiumAmount}%`;
  } else {
    const sign = product.premiumAmount >= 0 ? '+' : '-';
    return `${sign}$${Math.abs(product.premiumAmount).toFixed(2)}`;
  }
}

