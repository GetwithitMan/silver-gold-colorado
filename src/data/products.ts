export interface Product {
  id: string;
  name: string;
  subtitle?: string;
  metalType: 'gold' | 'silver';
  category: 'coins' | 'bars' | 'rounds' | 'fractional' | 'junk' | 'novelty';
  weight: number;
  weightUnit: 'oz' | 'gram';
  purity: string;
  premiumType: 'percent' | 'fixed';
  premiumAmount: number;
  country: string;
  mint?: string;
  year: string;
  description: string;
  image: string;
  isPopular?: boolean;
  isFeatured?: boolean;
  badge?: string;
  inStock?: boolean;
}

// Calculate price based on spot and premium
export function calculatePrice(product: Product, spotPrices: { gold: number; silver: number }): number {
  const spot = product.metalType === 'gold' ? spotPrices.gold : spotPrices.silver;

  // Convert grams to oz if needed
  const weightInOz = product.weightUnit === 'gram' ? product.weight / 31.1035 : product.weight;
  const spotValue = spot * weightInOz;

  if (product.premiumType === 'percent') {
    return spotValue * (1 + product.premiumAmount / 100);
  } else {
    return spotValue + product.premiumAmount;
  }
}

// Format premium for display
export function formatPremium(product: Product): string {
  if (product.premiumType === 'percent') {
    return `+${product.premiumAmount}%`;
  } else {
    return `+$${product.premiumAmount.toFixed(2)}`;
  }
}

